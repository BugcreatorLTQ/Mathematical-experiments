for k=1:10
   x1(k+1)=x1(k)+h;
   y1(k+1)=(1-h)*y1(k)+h*x1(k)+h;
   y2(k+1)=(y2(k)+h*x1(k+1)+h)/(1+h);
  % y3(k+1)= y3(k) + (h/2)* [ (1-h)*x1(k) + x1(k+1) + 2-h + (h-2)*y3(k)] ;
end
x1;y1;y2;    %（y1――向前欧拉解，y2――向后欧拉解）
x=0:0.1:1;
y=x+exp(-x); %（解析解）
plot(x,y,x1,y1,'k:',x1,y2,'r--')
